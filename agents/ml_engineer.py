def _build_pipeline(eda_text: str, task_name: str = "classification") -> str:
    """
    Build pipeline.py from the actual EDA report content on disk.
    Parses DROP / TRANSFORM / ENCODE / class_weight lines.
    """
    drop_cols, log_cols, encode_cols, class_weight = [], [], [], ""
    eda_note = "No EDA report found — using safe defaults."

    if eda_text and "[file not yet written]" not in eda_text:
        eda_note = "Recommendations applied from data_scientist/eda_report.md"
        for line in eda_text.splitlines():
            ll = line.lower().strip()
            # DROP
            if ll.startswith("drop:") or ("drop" in ll and "—" in line):
                for tok in line.replace(",", " ").replace(":", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in drop_cols:
                        drop_cols.append(t)
            # LOG TRANSFORM
            if "log" in ll and ("transform" in ll or "log1p" in ll or "skew" in ll):
                for tok in line.replace(",", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in log_cols:
                        log_cols.append(t)
            # ENCODE
            if ("one-hot" in ll or "encode" in ll) and "ENCODE" in line:
                for tok in line.replace(",", " ").replace(":", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in encode_cols:
                        encode_cols.append(t)
            # CLASS WEIGHT
            if "class_weight" in ll or "balanced" in ll or "imbalance" in ll:
                class_weight = "class_weight='balanced', "

    preprocess_lines = [f'    # {eda_note}']
    if drop_cols:
        preprocess_lines += [
            f'    drop_cols = {drop_cols}',
            '    df = df.drop(columns=[c for c in drop_cols if c in df.columns])',
        ]
    if log_cols:
        preprocess_lines += [
            f'    for col in {log_cols}:',
            '        if col in df.columns: df[col] = np.log1p(df[col])',
        ]
    if encode_cols:
        preprocess_lines += [
            f'    for col in {encode_cols}:',
            '        if col in df.columns:',
            '            dummies = pd.get_dummies(df[col], prefix=col, drop_first=True)',
            '            df = pd.concat([df.drop(col, axis=1), dummies], axis=1)',
        ]
    preprocess_lines += [
        '    X = df.drop(df.columns[-1], axis=1)',
        '    y = df.iloc[:, -1]',
        '    return train_test_split(X, y, test_size=0.2, stratify=y, random_state=42)',
    ]

