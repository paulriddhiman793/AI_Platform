    async def _check_and_connect_frontend(
        self, frontend_url: str, backend_url: str, task_id: str
    ) -> None:
        """
        The project's api_server.py lives at:
            {workspace.project_root}/ml_engineer/api_server.py

        This method:
          1. Checks if api_server.py exists in the project workspace
          2. Tries to reach it Гўв‚¬вЂќ if not running, launches it as a subprocess
          3. Sends POST /predict with a real test payload
          4. Reports the actual response in chat
        """
        import subprocess, sys, time, os

        api_server_path = workspace.project_root / "ml_engineer" / "api_server.py"
        project_dir     = workspace.project_root / "ml_engineer"
        api_port        = PROJECT_API_PORT
        project_api_url = "http://localhost:" + str(api_port)
        health_url      = project_api_url + "/health"
        predict_url     = project_api_url + "/predict"
        metrics_url     = project_api_url + "/metrics"

        await self.report(
            "Checking project API connectivity...\n"
            "  Project:    " + str(workspace.project_root) + "\n"
            "  api_server: " + str(api_server_path) + "\n"
            "  API URL:    " + project_api_url,
            task_id,
        )

        # ГўвЂќв‚¬ГўвЂќв‚¬ Helpers ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬ГўвЂќв‚¬
        def _get(url: str) -> tuple:
            try:
                req = urllib.request.Request(url, headers={"Accept": "application/json"})
                with urllib.request.urlopen(req, timeout=4) as resp:
                    body = _json.loads(resp.read().decode())
                    return resp.status, body, None
            except urllib.error.HTTPError as e:
                return e.code, {}, str(e)
            except Exception as exc:
                return 0, {}, str(exc)

        def _post(url: str, payload: dict) -> tuple:
            try:
                data = _json.dumps(payload).encode()
                req  = urllib.request.Request(
                    url, data=data,
                    headers={"Content-Type": "application/json", "Accept": "application/json"},
                    method="POST",
                )
                with urllib.request.urlopen(req, timeout=8) as resp:
                    body = _json.loads(resp.read().decode())
                    return resp.status, body, None
            except urllib.error.HTTPError as e:
                return e.code, {}, str(e)
            except Exception as exc:
                return 0, {}, str(exc)

        loop = asyncio.get_event_loop()

