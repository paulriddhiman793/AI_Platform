def _build_eda_report(task_name: str, da_context: str = "") -> str:
    """Build EDA report, incorporating any data analyst context."""
    incident_note = ""
    if da_context and "[file not yet written]" not in da_context:
        incident_note = "\n## Data Quality Incidents from Data Analyst\n" + da_context[:500]

    return f"""# EDA Report — {task_name}
Generated by Data Scientist Agent

## Dataset Overview
- Shape: 45,231 rows × 24 columns
- Target: `label` (binary: 0/1)
- Class balance: 78% negative / 22% positive → imbalanced

## Null Analysis
| Feature         | Null Rate | Action              |
|-----------------|-----------|---------------------|
| promo_clicks    | 41.2% ⚠   | DROP — broken source |
| last_login      | 12.1%     | Impute median        |
| referral_code   | 8.4%      | DROP                 |
| tenure          | 0.1%      | DROP (collinear)     |

## Distribution Analysis
- `account_age`: skew=2.3 → log-transform recommended
- `session_duration`: skew=1.8 → log-transform recommended
- `account_balance`: 0.3% extreme outliers — IQR capping applied

## Multicollinearity (Pearson r > 0.85)
- `age` ↔ `tenure`: r=0.91 → DROP tenure (keep age)

## Class Balance
- Positive class: 22% → use class_weight='balanced' in model

## Feature Recommendations
DROP:      promo_clicks, referral_code, tenure
TRANSFORM: log1p(account_age), log1p(session_duration)
ENCODE:    one-hot(region), one-hot(device_type)
MODEL:     class_weight='balanced'
{incident_note}
## Data Quality Incidents
- DQI-2024-012: promo_clicks null spike 2% → 41% starting Feb 8th (tracking pixel outage)
"""


def _build_feature_script(eda_content: str = "") -> str:
    """Build feature_engineering.py from actual EDA findings."""
    drop_cols, log_cols, encode_cols = [], [], []

    if eda_content and "[file not yet written]" not in eda_content:
        for line in eda_content.splitlines():
            ll = line.lower()
            if ll.startswith("drop:") or ("drop" in ll and ("—" in line or "-" in line)):
                for tok in line.replace(",", " ").replace(":", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in drop_cols:
                        drop_cols.append(t)
            if "log" in ll and ("transform" in ll or "log1p" in ll):
                for tok in line.replace(",", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in log_cols:
                        log_cols.append(t)
            if ("one-hot" in ll or "encode" in ll) and "ENCODE" in line:
                for tok in line.replace(",", " ").replace(":", " ").split():
                    t = tok.strip("` *-.()")
                    if t and "_" in t and 3 < len(t) < 35 and t not in encode_cols:
                        encode_cols.append(t)

    if not drop_cols:   drop_cols   = ["promo_clicks", "referral_code", "tenure"]
    if not log_cols:    log_cols    = ["account_age", "session_duration"]
    if not encode_cols: encode_cols = ["region", "device_type"]

    return f'''"""
feature_engineering.py
Generated by Data Scientist Agent — derived from eda_report.md
"""
import numpy as np
import pandas as pd


DROP_COLS    = {drop_cols}
LOG_COLS     = {log_cols}
ENCODE_COLS  = {encode_cols}


def engineer_features(df: pd.DataFrame) -> pd.DataFrame:
    """Apply all EDA-recommended transforms (sourced from eda_report.md)."""
    df = df.copy()
    df.drop(columns=[c for c in DROP_COLS if c in df.columns], inplace=True)
    for col in LOG_COLS:
        if col in df.columns:
            df[col] = np.log1p(df[col])
    for col in ENCODE_COLS:
        if col in df.columns:
            dummies = pd.get_dummies(df[col], prefix=col, drop_first=True)
            df = pd.concat([df.drop(col, axis=1), dummies], axis=1)
    return df
'''


class DataScientistAgent(BaseAgent):
    AGENT_ID      = "data_scientist"
    SYSTEM_PROMPT = SYSTEM_PROMPT

    async def handle_task(self, payload: dict) -> str | None:
        content    = payload.get("content", "")
        task_id    = payload.get("task_id")
        from_agent = payload.get("from", "orchestrator")
        c          = content.lower()

        # ── Data Analyst confirmed null history ──────────────────────────
        if from_agent == "data_analyst" and any(kw in c for kw in ["null", "history", "incident", "confirmed"]):
            # Read own EDA report and update it with DA's confirmation
            eda_text = self.read_file("data_scientist", "eda_report.md") or ""
            da_info  = content[:300]

            updated_eda = eda_text.replace(
                "DQI-2024-012: promo_clicks null spike",
                f"DQI-2024-012: promo_clicks null spike — CONFIRMED by Data Analyst: {da_info[:150]}"
            ) if eda_text else eda_text

            if updated_eda and updated_eda != eda_text:
                workspace.write("data_scientist", "eda_report.md", updated_eda, task_id)
                await self.report(
                    "eda_report.md updated with Data Analyst's null history confirmation.",
                    task_id
                )
            await self.message(
                "ml_engineer",
                "Data Analyst confirmed promo_clicks null root cause (Feb 8th pixel outage). "
                "Safe to drop permanently. EDA report updated on disk.",
                task_id
            )
            return None

        # ── ML Engineer asking for model validation ───────────────────────
        if from_agent == "ml_engineer" and any(kw in c for kw in ["validate", "check", "retrained", "new model"]):
            # Read the actual pipeline code and validate it
            pipeline = self.read_file("ml_engineer", "pipeline.py") or ""
            eda      = self.read_file("data_scientist", "eda_report.md") or ""

            issues   = []
            good     = []

            if pipeline and eda:
                # Check EDA recommendations are actually in the pipeline
                for line in eda.splitlines():
                    if "DROP:" in line:
                        drops = [t.strip("` ,.") for t in line.replace("DROP:", "").split(",")]
                        for col in drops:
                            if col and col not in pipeline:
                                issues.append(f"{col} not dropped in pipeline.py")
                            elif col and col in pipeline:
                                good.append(f"{col} correctly dropped")
                    if "class_weight='balanced'" in line and "class_weight='balanced'" in pipeline:
                        good.append("class_weight='balanced' applied")
                    if "log1p" in line.lower() and "log1p" in pipeline:
                        good.append("log-transforms applied")

            status = "PASSED" if not issues else f"ISSUES ({len(issues)})"
            await self.report(
                f"Model validation {status}.\n"
                f"Pipeline code read from workspace ({len(pipeline)} chars).\n"
                f"EDA compliance:\n" +
                "\n".join(f"  ✅ {g}" for g in good[:5]) +
                ("\n" + "\n".join(f"  ⚠ {i}" for i in issues[:3]) if issues else "") +
                "\nAccuracy gain: +1.7% from quantile transforms. No overfitting detected.",
                task_id
            )
            return None

        # ── EDA request ───────────────────────────────────────────────────
        if any(kw in c for kw in ["eda", "explore", "feature", "dataset", "train", "model", "build", "fraud", "churn", "pipeline"]):
            task_name = "fraud detection" if "fraud" in c else \
                        "churn prediction" if "churn" in c else "classification"

            await self.report(f"EDA started for {task_name}. Loading and profiling dataset...", task_id)
            await asyncio.sleep(0.3)

            # Read any existing incident log from Data Analyst
            da_context = self.read_file("data_analyst", "incident_log.md") or ""

            eda_report     = _build_eda_report(task_name, da_context)
            feature_script = _build_feature_script(eda_report)

            workspace.write("data_scientist", "eda_report.md",           eda_report,     task_id)
            workspace.write("data_scientist", "feature_engineering.py",  feature_script, task_id)

            await self.report(
                f"EDA complete for {task_name}.\n"
                f"  data_scientist/eda_report.md          ({len(eda_report)} chars)\n"
                f"  data_scientist/feature_engineering.py ({len(feature_script)} chars)\n\n"
                "Key findings:\n"
                "  - promo_clicks: 41% nulls — DROP\n"
                "  - account_age: skew 2.3 — log-transform\n"
                "  - age ↔ tenure: r=0.91 — drop tenure\n"
                "  - class_weight='balanced' recommended",
                task_id
            )

            # Notify Data Analyst about quality incident
            await self.message(
                "data_analyst",
                "Data quality incident: promo_clicks null rate 41%. "
                "Check historical logs for root cause.",
                task_id
            )

            # Send recommendations to ML Engineer
            await self.message(
                "ml_engineer",
                f"EDA complete for {task_name}. Recommendations:\n"
                "  DROP:      promo_clicks, referral_code, tenure\n"
                "  TRANSFORM: log1p(account_age), log1p(session_duration)\n"
                "  ENCODE:    one-hot(region), one-hot(device_type)\n"
                "  MODEL:     class_weight='balanced'\n"
                "Full report: data_scientist/eda_report.md",
                task_id
            )
            return None

        files = self.list_workspace_files("data_scientist")
        await self.report(
            f"Data Scientist ready.\n"
            f"Workspace files: {files if files else 'none yet'}\n"
            "No active quality incidents.",
            task_id
        )
        return None

