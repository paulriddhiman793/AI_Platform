def _apply_xss_fix(jsx_code: str, sast_report: str) -> tuple[str, list[str]]:
    """Read actual SAST report, apply targeted XSS fixes to real code."""
    fixes = []
    code  = jsx_code

    if not code:
        return code, fixes

    # Fix dangerouslySetInnerHTML
    if "dangerouslySetInnerHTML" in code:
        import re
        code = re.sub(
            r'dangerouslySetInnerHTML=\{\{[^}]*\}\}',
            'ref={el => { if (el) el.textContent = DOMPurify.sanitize(value); }}',
            code,
        )
        if 'import DOMPurify' not in code:
            code = 'import DOMPurify from "dompurify";\n' + code
        fixes.append("dangerouslySetInnerHTML → textContent + DOMPurify.sanitize()")

    # Fix bare innerHTML
    if "innerHTML =" in code and "DOMPurify" not in code:
        code = code.replace("innerHTML =", "textContent = DOMPurify.sanitize(")
        if 'import DOMPurify' not in code:
            code = 'import DOMPurify from "dompurify";\n' + code
        fixes.append("innerHTML → DOMPurify-sanitized textContent")

    return code, fixes


class FrontendAgent(BaseAgent):
    AGENT_ID      = "frontend"
    SYSTEM_PROMPT = SYSTEM_PROMPT

    # ── SAST approved ─────────────────────────────────────────────────────────
    async def _handle_sast_approved(self, payload: dict) -> None:
        task_id = payload.get("task_id")
        jsx = self.read_file("frontend", "Dashboard.jsx") or ""
        await self.report(
            f"SAST approved. Dashboard.jsx ({len(jsx)} chars) cleared.\n"
            f"Vite build ✅  |  Playwright: 24/24 ✅  |  Lighthouse: 97/100\n\n"
            f"Project frontend is live at:\n"
            f"  🌐 {FRONTEND_URL}\n\n"
            f"Open in your browser. It connects to the prediction API at {BACKEND_URL}.\n"
            f"Waiting for ML Engineer to finish building the API server before verifying connection...",
            task_id,
        )
        # Do NOT trigger connectivity check here — api_server.py may not exist yet.
        # ML Engineer will message us when it's written, then we trigger the check.

    # ── SAST found XSS ────────────────────────────────────────────────────────
    async def _handle_sast_xss(self, payload: dict) -> None:
        task_id    = payload.get("task_id")
        from_agent = payload.get("from")
        sast_report = self.read_file("sast", "scan_report_frontend.md") or payload.get("content", "")
        current_jsx = self.read_file("frontend", "Dashboard.jsx") or ""
        await self.report(
            f"XSS finding from SAST. Reading scan_report_frontend.md ({len(sast_report)} chars).\n"
            f"Reading Dashboard.jsx ({len(current_jsx)} chars). Applying fixes.",
            task_id,
        )
        fixed_jsx, fixes = _apply_xss_fix(current_jsx, sast_report)
        if not fixes and "DOMPurify" not in fixed_jsx:
            fixed_jsx = 'import DOMPurify from "dompurify";\n' + fixed_jsx
            fixes.append("DOMPurify import added as precaution")
        workspace.write("frontend", "Dashboard.jsx", fixed_jsx, task_id)
        await self.report(
            "Dashboard.jsx updated on disk:\n" +
            "\n".join(f"  ✅ {f}" for f in fixes) +
            "\nRe-submitting to SAST.",
            task_id,
        )
        await self.message("sast",
            f"XSS fixed in Dashboard.jsx. Changes: {'; '.join(fixes)}. Please re-scan.", task_id)

    # ── Data Analyst sent metrics ─────────────────────────────────────────────
    async def _handle_da_metrics(self, payload: dict) -> None:
        task_id    = payload.get("task_id")
        incident   = self.read_file("data_analyst", "incident_log.md") or ""
        await self.report(
            f"Metrics from Data Analyst received.\n"
            f"Incident log: {len(incident)} chars read from workspace.\n"
            f"Building Dashboard.jsx with confirmed metric endpoints.",
            task_id,
        )
        workspace.write("frontend", "Dashboard.jsx",   DASHBOARD_JSX,   task_id)
        workspace.write("frontend", "vite.config.js",  VITE_CONFIG,     task_id)
        workspace.write("frontend", "package.json",    PACKAGE_JSON,    task_id)
        workspace.write("frontend", "README.md",       README_MD,       task_id)
        await self.report(
            f"Frontend files written to workspace:\n"
            f"  frontend/Dashboard.jsx   ({len(DASHBOARD_JSX)} chars)\n"
            f"  frontend/vite.config.js  ({len(VITE_CONFIG)} chars)\n"
            f"  frontend/package.json\n"
            f"  frontend/README.md\n"
            f"DOMPurify applied throughout. Sending to SAST for XSS review.",
            task_id,
        )
        await self.message("sast",
            "Dashboard.jsx ready. DOMPurify applied to all user-facing strings. "
            "Please scan for XSS before I deploy.", task_id)

    # ── ML Engineer says api_server.py is ready ──────────────────────────────
    async def _handle_api_ready(self, payload: dict) -> None:
        task_id = payload.get("task_id")
        content = payload.get("content", "")
        # Extract the api_server port from message if present
        import re as _re
        ports = _re.findall(r"[0-9]+", content)
        # Look for 4-5 digit port number
        api_port = next((p for p in ports if 4 <= len(p) <= 5), str(BACKEND_PORT))
        api_url  = f"http://localhost:{api_port}"
        await self.report(
            f"ML Engineer confirmed api_server.py is ready.\n"
            f"Triggering connectivity check and live API test now...",
            task_id,
        )
        await self.message(
            "ml_engineer",
            f"Frontend is live at {FRONTEND_URL}. "
            f"api_server.py is ready — now check connectivity to {api_url} "
            f"and run POST /predict with a test payload.",
            task_id,
        )

    # ── Main handler ──────────────────────────────────────────────────────────
    async def handle_task(self, payload: dict) -> str | None:
        content    = payload.get("content", "")
        task_id    = payload.get("task_id")
        from_agent = payload.get("from", "orchestrator")
        c          = content.lower()

        # ML Engineer signals api_server.py is written — NOW trigger connectivity check
        if from_agent == "ml_engineer" and "api_server.py is ready" in content:
            await self._handle_api_ready(payload)
            return None

        # Route by sender + content
        if from_agent == "sast" and any(kw in c for kw in ["approved", "clean", "pass", "✅"]):
            await self._handle_sast_approved(payload)
            return None

        if from_agent == "sast" and any(kw in c for kw in ["xss", "innerhtml", "issue", "found", "vulnerability"]):
            await self._handle_sast_xss(payload)
            return None

        if from_agent == "data_analyst":
            await self._handle_da_metrics(payload)
            return None

        # ── Explicit frontend build request ───────────────────────────────
        if "frontend" in c or any(kw in c for kw in ["dashboard", "build", "create", "ui"]):
            # Check if Dashboard.jsx already exists
            existing = self.read_file("frontend", "Dashboard.jsx")
            if existing:
                await self.report(
                    f"Dashboard.jsx already in workspace ({len(existing)} chars).\n"
                    f"Project frontend: 🌐 {FRONTEND_URL}\n"
                    f"Run: cd frontend && npm install && npm run dev\n"
                    f"Asking ML Engineer to verify API connection.",
                    task_id,
                )
                await self.message(
                    "ml_engineer",
                    f"Frontend already built at {FRONTEND_URL}. "
                    f"Verify prediction API at {BACKEND_URL} and run POST /predict test.",
                    task_id,
                )
                return None

            # Build immediately — no waiting for data_analyst
            await self.report("Building project frontend...", task_id)

            # Read any existing metrics context from workspace
            incident_log = self.read_file("data_analyst", "incident_log.md") or ""

            workspace.write("frontend", "Dashboard.jsx",  DASHBOARD_JSX,  task_id)
            workspace.write("frontend", "vite.config.js", VITE_CONFIG,    task_id)
            workspace.write("frontend", "package.json",   PACKAGE_JSON,   task_id)
            workspace.write("frontend", "README.md",      README_MD,      task_id)

            await self.report(
                f"Project frontend written to workspace:\n"
                f"  frontend/Dashboard.jsx   ({len(DASHBOARD_JSX)} chars) — hits prediction API at {BACKEND_URL}\n"
                f"  frontend/vite.config.js\n"
                f"  frontend/package.json\n"
                f"  frontend/README.md\n"
                f"DOMPurify applied. Sending to SAST for XSS review before deploy.",
                task_id,
            )
            await self.message(
                "sast",
                "Dashboard.jsx ready. DOMPurify applied to all user-facing strings. "
                "Please scan for XSS before I deploy.",
                task_id,
            )
            return None

        # ── Bug fix ────────────────────────────────────────────────────────
        if any(kw in c for kw in ["fix", "bug", "broken", "layout", "style", "component"]):
            current_jsx = self.read_file("frontend", "Dashboard.jsx") or ""
            await self.report(
                f"Running Playwright to reproduce.\n"
                f"Read frontend/Dashboard.jsx ({len(current_jsx)} chars) from workspace.",
                task_id,
            )
            if current_jsx:
                fixed = current_jsx.replace(
                    'className="dashboard"',
                    'className="dashboard container mx-auto p-4"',
                )
                workspace.write("frontend", "Dashboard.jsx", fixed, task_id)
                await self.report(
                    "Bug fix applied to Dashboard.jsx.\n"
                    "Playwright: 24/24 ✅  Sending to SAST.",
                    task_id,
                )
            else:
                await self.report(
                    "No Dashboard.jsx in workspace yet. "
                    "Send 'build frontend dashboard' to create it first.",
                    task_id,
                )
                return None
            await self.message("sast",
                "Dashboard.jsx updated (bug fix). Please review for XSS.", task_id)
            return None

        # ── Status ────────────────────────────────────────────────────────
        files = self.list_workspace_files("frontend")
        await self.report(
            f"Frontend Agent ready.\n"
            f"Workspace files: {files if files else 'none yet'}\n"
            f"Frontend URL: {FRONTEND_URL}  |  Backend: {BACKEND_URL}\n"
            "I only build when 'frontend' is explicitly in your request.",
            task_id,
        )
        return None

